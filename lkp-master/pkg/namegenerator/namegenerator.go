var once sync.Once

func GetRandomName(retry int) string {
	once.Do(func() {
		rand.Seed(time.Now().UnixNano())
	})

	preCand, sufCand := entropy[rand.Intn(len(entropy))], entropy[rand.Intn(len(entropy))]
	name := preCand[rand.Intn(len(preCand))] + "_" + sufCand[rand.Intn(len(sufCand))]

	if retry > 0 {
		name += strconv.Itoa(rand.Intn(10 * int(math.Pow(2, float64(retry)))))
	}

	return name
}
